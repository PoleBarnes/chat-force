"""Mechanic B -- workflow execution analyzer.

After every task completes, Mechanic B inspects the execution data (and
LangSmith traces when available) for the run and produces:
  - Quality scores (task completion, efficiency, cost, SOP adherence, error rate)
  - Improvement proposals (prompt tweaks, tool changes, SOP updates)
  - A human-readable summary posted to the admin approvals channel

Proposals require explicit human approval before they take effect. Approved
proposals are committed to git; rejected proposals are logged and discarded.
"""

from __future__ import annotations

import logging
from enum import Enum
from pathlib import Path
from typing import Any

import yaml
from langgraph.graph import END, StateGraph
from pydantic import BaseModel, Field

from ..nodes.llm import call_claude, call_claude_structured

logger = logging.getLogger(__name__)

_PROJECT_ROOT = Path(__file__).resolve().parent.parent.parent


# ---------------------------------------------------------------------------
# State schema
# ---------------------------------------------------------------------------

class ProposalStatus(str, Enum):
    PENDING = "pending"
    APPROVED = "approved"
    REJECTED = "rejected"


class ImprovementProposal(BaseModel):
    """A single improvement proposal generated by Mechanic B."""
    id: str
    category: str = ""          # e.g. "prompt", "tool", "sop", "workflow"
    description: str = ""
    diff: str = ""              # Git-style diff of the proposed change
    confidence: float = 0.0     # 0.0 -- 1.0
    status: ProposalStatus = ProposalStatus.PENDING


class MechanicBState(BaseModel):
    """Typed state for the Mechanic B analysis graph."""

    # -- Input --
    run_id: str = ""
    trace_id: str = ""
    thread_id: str = ""
    workspace_id: str = ""

    # -- Trace data --
    trace_data: dict[str, Any] = Field(default_factory=dict)
    node_timings: dict[str, float] = Field(default_factory=dict)
    token_usage: dict[str, int] = Field(default_factory=dict)
    tool_calls: list[dict[str, Any]] = Field(default_factory=list)

    # -- Analysis --
    quality_scores: dict[str, float] = Field(default_factory=dict)
    proposals: list[ImprovementProposal] = Field(default_factory=list)
    summary: str = ""

    # -- Output --
    report: dict[str, Any] = Field(default_factory=dict)
    errors: list[str] = Field(default_factory=list)


# ---------------------------------------------------------------------------
# Helpers
# ---------------------------------------------------------------------------

def _load_evaluation_criteria() -> dict[str, Any]:
    """Load Mechanic B evaluation criteria from the platform config."""
    criteria_path = _PROJECT_ROOT / "platform" / "mechanics" / "evaluation-criteria.yaml"
    try:
        raw = yaml.safe_load(criteria_path.read_text(encoding="utf-8"))
        return raw.get("mechanic_b", {})
    except (FileNotFoundError, yaml.YAMLError, OSError) as exc:
        logger.warning("Could not load evaluation criteria: %s", exc)
        return {}


def _load_mechanic_b_prompt() -> str:
    """Load the Mechanic B system prompt from platform config."""
    prompt_path = _PROJECT_ROOT / "platform" / "mechanics" / "mechanic-b-prompt.md"
    try:
        return prompt_path.read_text(encoding="utf-8")
    except (FileNotFoundError, OSError) as exc:
        logger.warning("Could not load Mechanic B prompt: %s", exc)
        return (
            "You are Mechanic B, a workflow execution analyzer. "
            "Analyze execution data and propose improvements."
        )


