# ---------------------------------------------------------------------------
# Graph assembly
# ---------------------------------------------------------------------------

def build_graph() -> StateGraph:
    """Construct and compile the Mechanic B analysis graph."""

    builder = StateGraph(MechanicBState)

    builder.add_node("fetch_traces", fetch_traces)
    builder.add_node("analyze_quality", analyze_quality)
    builder.add_node("generate_proposals", generate_proposals)
    builder.add_node("compile_report", compile_report)

    builder.set_entry_point("fetch_traces")

    builder.add_edge("fetch_traces", "analyze_quality")
    builder.add_edge("analyze_quality", "generate_proposals")
    builder.add_edge("generate_proposals", "compile_report")
    builder.add_edge("compile_report", END)

    return builder


