"""Mechanic B — workflow execution analyzer.

After every task completes, Mechanic B inspects the LangSmith traces for the
run and produces:
  - Quality scores (accuracy, efficiency, cost)
  - Improvement proposals (prompt tweaks, tool changes, SOP updates)
  - A human-readable summary posted to the admin approvals channel

Proposals require explicit human approval before they take effect. Approved
proposals are committed to git; rejected proposals are logged and discarded.
"""

from __future__ import annotations

from enum import Enum
from typing import Any

from langgraph.graph import END, StateGraph
from pydantic import BaseModel, Field


# ---------------------------------------------------------------------------
# State schema
# ---------------------------------------------------------------------------

class ProposalStatus(str, Enum):
    PENDING = "pending"
    APPROVED = "approved"
    REJECTED = "rejected"


class ImprovementProposal(BaseModel):
    """A single improvement proposal generated by Mechanic B."""
    id: str
    category: str = ""          # e.g. "prompt", "tool", "sop", "workflow"
    description: str = ""
    diff: str = ""              # Git-style diff of the proposed change
    confidence: float = 0.0     # 0.0 – 1.0
    status: ProposalStatus = ProposalStatus.PENDING


class MechanicBState(BaseModel):
    """Typed state for the Mechanic B analysis graph."""

    # -- Input --
    run_id: str = ""
    trace_id: str = ""
    thread_id: str = ""
    workspace_id: str = ""

    # -- Trace data --
    trace_data: dict[str, Any] = Field(default_factory=dict)
    node_timings: dict[str, float] = Field(default_factory=dict)
    token_usage: dict[str, int] = Field(default_factory=dict)
    tool_calls: list[dict[str, Any]] = Field(default_factory=list)

    # -- Analysis --
    quality_scores: dict[str, float] = Field(default_factory=dict)
    proposals: list[ImprovementProposal] = Field(default_factory=list)
    summary: str = ""

    # -- Output --
    report: dict[str, Any] = Field(default_factory=dict)
    errors: list[str] = Field(default_factory=list)


